#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Functions
#--------------------------------------------------------------------------------------------------

upscale()
{
    cd "$PWD/dist/room/$1"/content

    sh upscale.sh

    cd -
}

renderBase()
{
    sh render.sh "$1"
}

render()
{
    renderBase "room/$1"
}

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

read -p "Run upscale ? (yes/no) " REPLY

if [ "$REPLY" != "yes" ]; then exit 1; fi

